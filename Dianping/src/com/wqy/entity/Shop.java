package com.wqy.entity;

public class Shop {
	private String shop_id;
	private String shop_name;
	private String shop_tel;
	private String shop_address;
	private String shop_area;
	private String shop_open_time;
	private String shop_lon;
	private String shop_lat;
	private String shop_traffic_info;
	public String getShop_id() {
		return shop_id;
	}
	public void setShop_id(String shopId) {
		shop_id = shopId;
	}
	public String getShop_name() {
		return shop_name;
	}
	public void setShop_name(String shopName) {
		shop_name = shopName;
	}
	public String getShop_tel() {
		return shop_tel;
	}
	public void setShop_tel(String shopTel) {
		shop_tel = shopTel;
	}
	public String getShop_address() {
		return shop_address;
	}
	public void setShop_address(String shopAddress) {
		shop_address = shopAddress;
	}
	public String getShop_area() {
		return shop_area;
	}
	public void setShop_area(String shopArea) {
		shop_area = shopArea;
	}
	public String getShop_open_time() {
		return shop_open_time;
	}
	public void setShop_open_time(String shopOpenTime) {
		shop_open_time = shopOpenTime;
	}
	public String getShop_lon() {
		return shop_lon;
	}
	public void setShop_lon(String shopLon) {
		shop_lon = shopLon;
	}
	public String getShop_lat() {
		return shop_lat;
	}
	public void setShop_lat(String shopLat) {
		shop_lat = shopLat;
	}
	public String getShop_traffic_info() {
		return shop_traffic_info;
	}
	public void setShop_traffic_info(String shopTrafficInfo) {
		shop_traffic_info = shopTrafficInfo;
	}

}
